package ItSchool;

public class JavaStringConcatenation {
    public static void main(String[] args) {
        String firstName = "Andrei";
        String lastName = "Gabriel";
        System.out.println(firstName + " " + lastName);
        String myName1 = "Lazar ";
        String myName2 = "Marian";
        System.out.println(myName1.concat(myName2));
    }
}
