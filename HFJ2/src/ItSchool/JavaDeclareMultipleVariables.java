package ItSchool;

public class JavaDeclareMultipleVariables {
    public static void main(String[] args) {
        int x = 5;
        int y = 8;
        int z = 9;
        System.out.println(x + y + z);

        int o = 7, u = 8, l = 9;
        System.out.println(o + u + l);

        int a, s, d;
        a = s = d = 100;
        System.out.println(a + s + d);
    }


}
