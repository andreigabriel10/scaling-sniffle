package ItSchool;

public class JavaNumbersAndStrings {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;
        int z = x + y;
        System.out.println(z);
        String a = "10";
        String b = "20";
        String c = a + b;
        System.out.println(c);
        String u = "10";
        int v = 20;
        String n = u + v;
        System.out.println(n);


    }
}
